// JS 연산자
// 1) 대입 연산자 2) 산술 연산자 3) 비교 연산자 4) 논리 연산자

// 1. 산술 연산자
let a = 20;
let b = 3;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);

// 2. 비교 연산자
// 1) 동등 비교
// 같다 : == , ===
// 다르다 : != , !==
// == , != : 값만 비교, 자료형은 비교 X
// === , !== : 값과 자료형 모두 비교
console.log(1 == "1"); // true
console.log(1 != "1"); // false
console.log(1 === "1"); // flase
console.log(1 !== "1"); // true